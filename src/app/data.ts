export function getRandomData(n = 1000) {
  const data = [];
  for (let i = 0; i < n; i++) {
    const born = Math.round(Math.random() * 200) + 1800;
    data.push({
      name: names[Math.floor(Math.random() * names.length)],
      parent: names[Math.floor(Math.random() * names.length)],
      born,
      died: born + Math.round(Math.random() * 120),
      height: (Math.random() * 84).toFixed(1).split(".").join("' ") + '"',
    });
  }
  return data;
}
